#!/usr/bin/env node

/**
 * build-word-map.js
 * 
 * Generates js/word-map.js from data/dictionary.js
 * 
 * This script:
 * 1. Reads all (c, p) pairs from the dictionary
 * 2. Adds proper nouns with capitalized pinyin
 * 3. Adds punctuation mappings to empty string
 * 4. Generates a flat JavaScript object for runtime lookup
 */

const fs = require('fs');
const path = require('path');

// Define proper nouns with capitalized pinyin
const PROPER_NOUNS = {
  "李华": "Lǐ Huá",
  "王芳": "Wáng Fāng",
  "陈明": "Chén Míng",
  "李明": "Lǐ Míng",
  "北京": "Běijīng"
};

// Define punctuation mappings (map to empty string for no ruby wrapper)
const PUNCTUATION = {
  "。": "",
  "，": "",
  "！": "",
  "？": "",
  "、": "",
  "：": "",
  "；": "",
  "…": ""
};

function main() {
  console.log('🔨 Building WORD_MAP from dictionary...\n');

  // Read the dictionary file
  const dictionaryPath = path.join(__dirname, '..', 'data', 'dictionary.js');
  
  if (!fs.existsSync(dictionaryPath)) {
    console.error(`❌ Error: Dictionary file not found at ${dictionaryPath}`);
    process.exit(1);
  }

  const dictionaryContent = fs.readFileSync(dictionaryPath, 'utf8');
  
  // Extract the dictionary object
  // The file assigns to window.HSK_DICTIONARY, so we need to parse it
  const dictionaryMatch = dictionaryContent.match(/window\.HSK_DICTIONARY\s*=\s*({[\s\S]+})\s*;?\s*$/);
  
  if (!dictionaryMatch) {
    console.error('❌ Error: Could not parse dictionary structure');
    process.exit(1);
  }

  let dictionary;
  try {
    // Use eval in a safe context to parse the object
    dictionary = eval(`(${dictionaryMatch[1]})`);
  } catch (error) {
    console.error('❌ Error parsing dictionary JSON:', error.message);
    process.exit(1);
  }

  if (!dictionary.words || !Array.isArray(dictionary.words)) {
    console.error('❌ Error: Dictionary does not contain a "words" array');
    process.exit(1);
  }

  console.log(`📖 Found ${dictionary.words.length} words in dictionary`);

  // Build the WORD_MAP
  const wordMap = {};

  // Add all dictionary entries
  let dictionaryCount = 0;
  for (const word of dictionary.words) {
    if (word.c && word.p) {
      wordMap[word.c] = word.p;
      dictionaryCount++;
    }
  }

  console.log(`✅ Added ${dictionaryCount} dictionary entries`);

  // Add proper nouns
  let properNounCount = 0;
  for (const [chinese, pinyin] of Object.entries(PROPER_NOUNS)) {
    wordMap[chinese] = pinyin;
    properNounCount++;
  }

  console.log(`✅ Added ${properNounCount} proper nouns`);

  // Add punctuation
  let punctuationCount = 0;
  for (const [chinese, pinyin] of Object.entries(PUNCTUATION)) {
    wordMap[chinese] = pinyin;
    punctuationCount++;
  }

  console.log(`✅ Added ${punctuationCount} punctuation marks`);

  // Generate the output file
  const outputLines = [
    '/**',
    ' * word-map.js',
    ' * ',
    ' * Central pinyin lookup table for the HSK Learning App.',
    ' * Generated automatically by scripts/build-word-map.js',
    ' * ',
    ' * DO NOT EDIT THIS FILE MANUALLY',
    ' * Run `node scripts/build-word-map.js` to regenerate',
    ' */',
    '',
    'const WORD_MAP = {'
  ];

  // Sort keys for consistent output and easier diffing
  const sortedKeys = Object.keys(wordMap).sort();

  for (const key of sortedKeys) {
    const value = wordMap[key];
    // Escape quotes in the value
    const escapedValue = value.replace(/"/g, '\\"');
    outputLines.push(`  "${key}": "${escapedValue}",`);
  }

  // Remove trailing comma from last entry
  if (outputLines.length > 10) {
    const lastLine = outputLines[outputLines.length - 1];
    outputLines[outputLines.length - 1] = lastLine.slice(0, -1);
  }

  outputLines.push('};');
  outputLines.push('');
  outputLines.push('// Export for browser usage');
  outputLines.push('window.WORD_MAP = WORD_MAP;');
  outputLines.push('');

  // Write the output file
  const outputPath = path.join(__dirname, '..', 'js', 'word-map.js');
  fs.writeFileSync(outputPath, outputLines.join('\n'), 'utf8');

  console.log(`\n✨ Successfully generated ${outputPath}`);
  console.log(`📊 Total entries: ${sortedKeys.length}`);
  console.log(`   - Dictionary words: ${dictionaryCount}`);
  console.log(`   - Proper nouns: ${properNounCount}`);
  console.log(`   - Punctuation: ${punctuationCount}`);
}

// Run the script
main();
